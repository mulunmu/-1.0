def name_initials(name: str) -> str:
    """提取企业名拼音首字母串（大写）"""
    parts: list[str] = []
    for ch in name:
        if ch.isascii() and ch.isalnum():
            parts.append(ch.upper())
        elif ch in _PINYIN:
            parts.append(_PINYIN[ch])
    return "".join(parts)
